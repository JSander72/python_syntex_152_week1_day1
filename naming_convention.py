## Naming Converntions

#snake_case
    #Python standard naming convention 
    # always use lowercase letters, 
    # always underscored between words

# camelCase 
    # JavaScript standard naming convention 
    # always start with a lowercase letter
    # Capitalize the first letter of each word after the first letter

# PascalCase 
    # Similar to camelCase, but capitalize the first letter of each word
    # Java and TypeScript standard naming convention 
# kebab-case 
    # Convert words into hyphens to separate them
    # CSS and SASS standard naming convention

# ALL CAPS
    # represents a piece of data that will never change 

